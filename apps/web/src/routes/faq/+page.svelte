  <h1>Вопросы и ответы</h1>

  <h2>Что доказывают хеши?</h2>
  <p>
    Каждое событие в реестре содержит хеш предыдущего события. Это образует хеш-цепочку: если
    изменить любое событие задним числом, все последующие хеши изменятся. Хеш-цепочка доказывает,
    что публичная история не была переписана незаметно.
  </p>
  <p>
    Хеши <strong>не доказывают</strong>, что оператор действительно купил сертификат или что чек
    подлинный. Они доказывают только целостность последовательности записей.
  </p>

  <h2>Что доказывают якоря?</h2>
  <p>
    Якорь (anchor) — это транзакция в блокчейне Solana, которая содержит хеш текущего состояния
    реестра (HEAD) в поле Memo. Поскольку транзакции Solana имеют временную метку и не могут быть
    подделаны задним числом, якорь доказывает, что определённый HEAD реестра существовал на момент
    публикации якоря.
  </p>
  <p>
    Важно: якорь фиксирует HEAD, существовавший <strong>до</strong> события публикации якоря. Сама
    запись <code>anchor_published</code> будет покрыта следующим якорем.
  </p>

  <h2>Что НЕ доказывают чеки?</h2>
  <p>
    Номер чека (receipt_ref) в реестре не доказывает подлинность чека. Оператор может записать любой
    номер. Реестр гарантирует, что запись не была изменена задним числом, но не гарантирует, что чек
    был действительно куплен у указанного сервиса.
  </p>
  <p>
    Это честное ограничение системы: мы доказываем целостность записей, а не подлинность каждого
    чека. Проверка чеков остаётся на стороне сервисов-партнёров.
  </p>

  <h2>Почему переводы видны в Solana?</h2>
  <p>
    Все транзакции SPL-токенов в блокчейне Solana публичны по своей природе. Адрес кошелька донора,
    сумма перевода и адрес получателя видны в обозревателях блокчейна (Solscan, Solana Explorer).
  </p>
  <p>
    Это означает, что адрес вашего кошелька может быть связан с пожертвованием через анализ
    блокчейна. Open Care не публикует адреса доноров в реестре, но они видны на уровне блокчейна.
  </p>

  <h2>Почему данные Telegram не публикуются?</h2>
  <p>
    Приватность получателей зависит от границы бота. Telegram ID, chat ID, внутренние идентификаторы
    и имена получателей существуют только внутри бота и никогда не попадают в публичный реестр.
  </p>
  <p>
    Публичный реестр содержит только обезличенные ссылки (<code>benpub_...</code>), которые
    генерируются сервером и не могут быть обратно связаны с получателем через публичные данные.
  </p>

  <h2>Почему оператор — ручной?</h2>
  <p>
    В текущей версии (MVP) оператор вручную покупает сертификаты и записывает чеки. Это осознанное
    ограничение:
  </p>
  <ul>
    <li>Автоматическая закупка сертификатов требует интеграции с API сервисов-партнёров.</li>
    <li>Ручной цикл позволяет запустить проект быстрее и проверить востребованность.</li>
    <li>Автоматизация конвертации — цель следующих версий.</li>
  </ul>

