<section class="section">
	<h2>О проверке и якорях</h2>

	<div class="faq-item">
		<h3>Что доказывают хеши в реестре?</h3>
		<p>Хеши доказывают, что последовательность записей <strong>не была изменена</strong> после их создания. Каждая запись содержит хеш предыдущей, образуя цепочку. Изменение любой записи потребует пересчёта всех последующих хешей.</p>
	</div>

	<div class="faq-item">
		<h3>Что доказывают якоря?</h3>
		<p>Якоря записывают текущий HEAD реестра в блокчейн Solana через Memo-транзакцию. Это доказывает, что на момент якоря реестр имел определённое состояние. Якори покрываются следующими якорями, создавая непрерывную цепочку от генезиса до текущего состояния.</p>
	</div>

	<div class="faq-item">
		<h3>Чего якоря НЕ доказывают?</h3>
		<p>Якоря <strong>не доказывают</strong> подлинность чеков, получение подарочного сертификата получателем или качество оказанной услуги. Они доказывают только целостность публичной истории записей.</p>
	</div>

	<div class="faq-item">
		<h3>Почему нет якоря с самого начала?</h3>
		<p>Первый якорь появляется после того, как в реестре достаточно записей. До появления якоря хеш-цепочка реестра всё равно позволяет проверить целостность всех записей.</p>
	</div>
</section>

<section class="section">
	<h2>О пожертвованиях и приватности</h2>

	<div class="faq-item">
		<h3>Видны ли мои переводы в блокчейне?</h3>
		<p>Да. Переводы USDC на Solana публичны. Адрес вашего кошелька и сумма перевода видны через обозреватели блоков (Solscan, Solana Explorer). Однако <strong>ваше имя, контакты и Telegram ID не публикуются</strong> на этом сайте.</p>
	</div>

	<div class="faq-item">
		<h3>Почему данные Telegram не видны публично?</h3>
		<p>Telegram ID, внутренние идентификаторы чатов, номера телефонов и имена — это личные данные. Мы публикуем только серверно-сгенерированный <code>public_beneficiary_ref</code> (вида <code>benpub_XXXXXXXXXXXXXXXX</code>), который не позволяет идентифицировать человека.</p>
	</div>

	<div class="faq-item">
		<h3>Могу ли я указать Memo при переводе?</h3>
		<p>Технически да, но мы <strong>настоятельно не рекомендуем</strong> указывать в Memo имена, контакты или данные получателя. On-chain Memo виден публично. Сайт не публикует donor memo по умолчанию.</p>
	</div>

	<div class="faq-item">
		<h3>Почему покупка подарочных сертификатов — ручная?</h3>
		<p>Автоматическая покупка сертификатов увеличивает сложность и риски ошибок. Ручной процесс позволяет оператору проверять запросы и контекст. Все выплаты записаны в реестре публично.</p>
	</div>
</section>

<section class="section">
	<h2>О проекте</h2>

	<div class="faq-item">
		<h3>Кто управляет проектом?</h3>
		<p>Оператор — человек, который вручную покупает подарочные сертификаты и записывает выплаты в реестр. Подробности на странице <a href="/about">О проекте</a>.</p>
	</div>

	<div class="faq-item">
		<h3>Что делать, если я нашёл несовпадение хеша?</h3>
		<p><a href="/contact">Сообщите нам</a> с указанием наблюдаемого хеша, URL страницы и примерного времени. Мы не запрашиваем приватные ключи, seed-фразы, Telegram ID или реальные имена.</p>
	</div>
</section>

