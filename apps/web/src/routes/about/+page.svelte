  <h1>О проекте</h1>

  <h2>Что такое Open Care</h2>
  <p>
    Open Care — это прозрачная благотворительная платформа на Solana. Мы помогаем людям оплатить
    сессии терапии через крипто-пожертвования, сохраняя полную публичную прослеживаемость движения
    средств и приватность получателей.
  </p>

  <h2>Как устроен процесс</h2>
  <ol>
    <li>
      <strong>Донор</strong> отправляет SPL USDC на ATA-адрес хранилища через любой кошелёк Solana.
    </li>
    <li>
      <strong>Система</strong> отслеживает финализированные переводы и записывает их в публичный
      реестр как события <code>donation_confirmed</code>.
    </li>
    <li>
      <strong>Оператор</strong> вручную покупает сертификаты на терапию (Alter, Yasno, Zigmund) и
      записывает каждый чек в реестр как <code>disbursement_recorded</code>.
    </li>
    <li>
      <strong>Бот</strong> доставляет код сертификата получателю приватно через Telegram, не раскрывая
      код в публичном реестре.
    </li>
    <li>
      <strong>Реестр</strong> фиксирует все события в хеш-цепочку и периодически публикует якорь (anchor)
      в блокчейн Solana.
    </li>
  </ol>

  <h2>Ручной цикл конвертации</h2>
  <p>
    В текущей версии (MVP) оператор вручную конвертирует пожертвования в сертификаты на терапию. Это
    означает:
  </p>
  <ul>
    <li>Оператор отслеживает баланс хранилища.</li>
    <li>При накоплении достаточной суммы оператор приобретает сертификаты у сервисов-партнёров.</li>
    <li>Каждая покупка записывается в реестр с номером чека и обезличенной ссылкой.</li>
    <li>Автоматизация закупки сертификатов — цель будущих версий.</li>
  </ul>

  <h2>Разделение кошельков</h2>
  <p>Проект использует два отдельных кошелька:</p>
  <ul>
    <li>
      <strong>Кошелёк хранилища (treasury)</strong> — принимает пожертвования в USDC. Средства используются
      только для покупки сертификатов.
    </li>
    <li>
      <strong>Кошелёк якоря (anchor wallet)</strong> — содержит небольшой запас SOL для оплаты комиссий
      при публикации якорей в блокчейн Solana.
    </li>
  </ul>
  <p>
    Разделение гарантирует, что SOL для якорей не зависит от баланса пожертвований, а
    USDC-пожертвования не тратятся на комиссии блокчейна.
  </p>

  <h2>Почему получатели остаются приватными</h2>
  <p>
    Публичный реестр содержит только обезличенные ссылки (<code>benpub_...</code>) и номера чеков.
    Связь между обезличенной ссылкой и реальным получателем существует только внутри бота и не
    раскрывается в реестре. Telegram ID, имена и контакты получателей никогда не публикуются.
  </p>

